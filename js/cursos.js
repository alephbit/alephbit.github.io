var get_pagseguro = function(farr, arr)
{
	var i, sum;

	if (!farr)
		return false;

	sum = 0;
	for (i = 0; i < farr.length - 1; i++)
		if ($('#' + farr[i]).is(':checked'))
			sum |= 1 << i;

	if (sum > 0 && arr[sum]) {
		$('#' + curso_ml_fields[i]).val(arr[sum]);
		return true;
	} else {
		alert("Não foi possível realizar o pagamento\nFavor entrar em contato.");
		return false;
	}
}
	
